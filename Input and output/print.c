#include <stdio.h>

int main()
{
    int age = 12;
    printf("age is %d\n",age); // %d is a format specifier for int variable and \n for line break

    float pi = 3.14;
    printf("value of pi is %f\n",pi); // %f for floating-point value

    char data = 'A';
    printf("value of data %c\n",data); // %c for character value
}